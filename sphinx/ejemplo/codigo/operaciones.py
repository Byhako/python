import libreria

def suma(a, b):
  """
  |

  This functions makes the **addition** of the numbers **a**
  and **b**.

  :arg a: First addend.
  :type a: float

  :arg b: Second addend.
  :type b: float

  :returns: Addition.
  :rtype: float

  Where::

    Addition = a + b

  |

  """
  return libreria.func_addition(a,b)

def resta(a, b):
  """
  |

  This functions makes the **Difference** of the numbers **a**
  and **b**.

  :arg a: First addend.
  :type a: float

  :arg b: Second addend.
  :type b: float

  :returns: Difference.
  :rtype: float

  Where::

    Difference = a + b

  |

  """
  return libreria.func_subtraction(a,b)
