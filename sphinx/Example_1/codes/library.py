def func_addition(num1, num2):
    """
    .. Doc Begins

    |

    This functions makes the **addition** of the numbers **num1**
    and **num2**.

    :arg num1: First addend.
    :type num1: float

    :arg num2: Second addend.
    :type num2: float

    :returns: Addition.
    :rtype: float

    Where::

        Addition = num1 + num2

    |

    .. Doc Finish
    """
    return num1 + num2

def func_subtraction(num1, num2):
    """
    .. Doc Begins

    |

    This functions makes the subtraction of the numbers **num1**
    and **num2**.

    :arg num1: Minuend.
    :type num1: float

    :arg num2: Subtrahend.
    :type num2: float

    :returns: Difference.
    :rtype: float

    Where::

        Difference = num1 - num2

    |

    .. Doc Finish
    """
    return num1 - num2
